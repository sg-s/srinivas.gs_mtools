% named colors in Apple's Aqua colorscheme
function C = aqua(name)

switch name
case 'blue'
	C = [0 122 255]/255;
case 'brown'
	C = [162 132 94]/255;
case 'gray'
	C = [142 142 147]/255;
case 'green'
	C = [40 205 65]/255;
case 'indigo'
	C = [88 86 214]/255;
case 'orange'
	C = [255 149 0]/255;
case 'pink'
	C = [255 45 85]/255;
case 'purple'
	C = [175 82 222]/255;
case 'red'
	C = [255 59 48]/255;
case 'teal'
	C = [90 200 250]/255;
case 'yellow'
	C = [255 204 0]/255;
case 'lime'
	C = [142 250 0]/255;
end

